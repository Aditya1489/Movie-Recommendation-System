def fix_posters():
    db = SessionLocal()
    try:
        for title, poster_url in POSTER_FIXES.items():
            movie = db.query(Movies).filter(Movies.title == title).first()
            if movie:
                movie.poster_url = poster_url
                print(f"Updated poster for: {title}")
        
        db.commit()
        print("All posters updated!")
    except Exception as e:
        print(f"Error: {e}")
        db.rollback()
    finally:
        db.close()


if __name__ == "__main__":
    fix_posters()
